//ログインボタン（日本語）
$(function() {
	$(".login_jp_path").on("mouseover", function() {
		$(this).css({"filter": "opacity(0%)"})
		$(".login_jp_click_path").css({"filter": "opacity(100%)"})
		$(".registration_jp_path").css({"filter": "opacity(30%)"})
		$(".no_login_jp_path").css({"filter": "opacity(30%)"})
	}).on("mouseout", function() {
		$(this).css({"filter": "opacity(100%)"})
		$(".registration_jp_path").css({"filter": "opacity(100%)"})
		$(".no_login_jp_path").css({"filter": "opacity(100%)"})
		$(".no_login_click_jp_path").css({"filter": "opacity(0%)"})
		$(".top_jp_click_path").css({"filter": "opacity(100%)"})
		$(".click_jp_top").fadeOut(500);
	})
});
